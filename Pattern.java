class Pattern //class name "large"
{
	public static void main(String[] args)//main method
	{
		int i,j;//variable declaration
		for(i=5;i>=1;i--)
		{
			for(j=1;j<=i;j++)
			{
				System.out.print("*");//print *
			}
			System.out.println();//move on next line
		}
	}
}	