//Arithmetic operation of 3 numbers
class Arithmetic
{
	public static void main(String[] args)
	{
		int a,b,c,d,x,y,z;
		x=10;
		y=4;
		z=2;
		a=x+(y+z);
		b=y+(x-z);
		c=x*(y*z);
		d=x%(y/z);
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}
}	
		
	